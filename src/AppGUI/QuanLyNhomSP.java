    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        if (this.searchText.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Nhập tên nhóm muốn tìm kiếm!");
        } else {
            ArrayList<GroupClothes> listGroupClone = new ArrayList<>();
            for (int i = 0; i < this.listGroup.size(); i++) {
                if (this.listGroup.get(i).getName().indexOf(this.searchText.getText()) != -1) {
                    listGroupClone.add(this.listGroup.get(i));
                }
            }
            this.groupTableModel.setRowCount(0);
            for (int i = 0; i < listGroupClone.size(); i++) {
                this.groupTableModel.addRow(new Object[]{
                    listGroupClone.get(i).getID(),
                    listGroupClone.get(i).getName(),});
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed


