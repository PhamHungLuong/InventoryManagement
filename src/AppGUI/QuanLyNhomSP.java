    private void addGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addGroupActionPerformed
        if (this.idGroupText.getText().length() == 0 || this.nameGroupText.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập đầy đủ thông tin vào ô trống");
        } else {
            GroupClothes newGR = new GroupClothes(Integer.parseInt(this.idGroupText.getText()), this.nameGroupText.getText());
            String checked = "";
            for (int i = 0; i <this.listGroup.size(); i++) {
                if (newGR.getID() == this.listGroup.get(i).getID()) {
                    checked = "fail";
                    break;
                }
            }
            if (checked.compareTo("fail") == 0) {
                JOptionPane.showMessageDialog(rootPane, "ID đã tồn tại !");
            } else {
                this.listGroup.add(newGR);
//                IOfile.writeGroupClothe(this.listGroup);
                GroupClothesDAO.getInstance().Add(newGR);
                this.idGroupText.setText("");
                this.nameGroupText.setText("");

                this.showListGroup();
                JOptionPane.showMessageDialog(rootPane, "Thêm nhóm thành công !");
            }
        }
    }//GEN-LAST:event_addGroupActionPerformed

    private void updateGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateGroupActionPerformed
        if (this.selectPosition == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn nhóm muốn cập nhật!");
        } else {
            int index = -1;
            for (int i = 0; i < this.listGroup.size(); i++) {
                if (Integer.parseInt(this.idGroupText.getText()) == this.listGroup.get(i).getID()) {
                    index = i;
                    break;
                }
            }
//            GroupClothes newGR = new GroupClothes(Integer.parseInt(this.idGroupText.getText()), this.nameGroupText.getText());
            this.listGroup.get(index).changePropeties(this.nameGroupText.getText());
//            IOfile.writeGroupClothe(this.listGroup);
            GroupClothesDAO.getInstance().Update(this.listGroup.get(index));
            this.idGroupText.setText("");
            this.nameGroupText.setText("");
            this.selectPosition = -1;

            this.showListGroup();
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công !");
        }
    }//GEN-LAST:event_updateGroupActionPerformed

    private void deleteGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteGroupActionPerformed
        if (this.selectPosition == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn nhóm muốn xóa!");
        } else {
            int index = -1;
            for (int i = 0; i < this.listGroup.size(); i++) {
                if (Integer.parseInt(this.idGroupText.getText()) == this.listGroup.get(i).getID()) {
                    index = i;
                    break;
                }
            }
            String checked = "";
            for (int i = 0; i < ClothesDAO.getInstance().selectAll().size(); i++) {
                if (this.listGroup.get(index).getID() == ClothesDAO.getInstance().selectAll().get(i).getIdNhom()) {
                    checked = "fail";
                    break;
                }
            }
            if (checked.compareTo("fail") == 0) {
                JOptionPane.showMessageDialog(rootPane, "Không thể xóa do có sản phẩm thuộc nhóm này");
            } else {
//                GroupClothes newGR = new GroupClothes(Integer.parseInt(this.idGroupText.getText()), this.nameGroupText.getText());
//                this.listGroup.remove(index);
//                IOfile.writeGroupClothe(this.listGroup);
                GroupClothesDAO.getInstance().Delete(this.listGroup.get(index));
                this.listGroup.remove(index);
                this.idGroupText.setText("");
                this.nameGroupText.setText("");
                this.selectPosition = -1;

                this.showListGroup();
                JOptionPane.showMessageDialog(rootPane, "Xóa nhóm thành công !");
            }
        }
    }//GEN-LAST:event_deleteGroupActionPerformed

